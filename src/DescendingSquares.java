/* 2. Your pseudocode algorithm here
 * Create a for loop
 * 	{
 * 		declare an integer called i to be equal to 10
 * 		have said integer to be greater than 0
 * 		and have said integer to go backwards with i--
 * 		After print out i to multiply by itself following a space to space out numbers.


*/

public class DescendingSquares {

	public static void main(String[] args) {
		for (int i = 10; i > 0; i--) {
			System.out.print(i*i + " ");
		}

	}

}
